    private void showInfor(){
        // lấy operating fee thông qua controller
        try{
            event = eventController.getEvent(idEvent);
        }catch(Exception e){
            noti.showNotiError("Có lỗi: " + e.toString());
        }
        System.out.println(idEvent);
        System.out.println(event);
        // Hiển thị lên màn hình thông tin của khoản phí
        if(event != null){
            
            txtName.setText(event.getNameEvent());
            txtAddress.setText(event.getAddress());
            txtEnd.setText(event.getEndDay());
            txtNumber.setText(event.getNumberOfStudent() + "");
            txtStart.setText(event.getStartDay());
        }
        else{
            noti.showNotiError("Có lỗi: không có sự kiện nào được tìm thấy trong database");
        }
    }
