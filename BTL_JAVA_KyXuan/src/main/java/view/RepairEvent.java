    private void txtRepairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtRepairActionPerformed
        // TODO add your handling code here:
        
        String name = txtName.getText().trim();
        String address = txtAddress.getText().trim();
        String end = txtEnd.getText().trim();
        Integer number = Integer.parseInt(txtNumber.getText().trim());
        String start = txtStart.getText().trim();
        try{
            if(eventController.repairEvent(idEvent, name, start, end, number, address)){
                noti.showNotiInformation("Thay đổi thông tin sự kiện thành công");
                new QLEvent().setVisible(true);
                this.setVisible(false);
            }
            else{
                noti.showNotiError("Không tìm thấy sự kiện này");
            }
        }catch(Exception e){
            noti.showNotiError("Có lỗi: " + e.toString());
        }
        
    }//GEN-LAST:event_txtRepairActionPerformed

